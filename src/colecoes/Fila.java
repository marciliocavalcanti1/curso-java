package colecoes;

import java.util.LinkedList;
import java.util.Queue;

public class Fila {
	public static void main(String[] args) {

		Queue<String> fila = new LinkedList<>();

		// Offer e Add -> adicionam elementos na fila
		// Diferença é o comportamento ocorre quando a fila está cheia!
		fila.add("Ana"); // retorna false
		fila.offer("Bia"); // lança uma exceção
		fila.add("Carlos");
		fila.offer("Daniel");
		fila.add("Rafaela");
		fila.offer("Gui");

		// Peek e Element -> obter o próximo elementos da fila (sem remover)
		// Diferença é o comportamento ocorre quando a fila está vazia!
		System.out.println(fila.peek()); // retorna null
		System.out.println(fila.peek());
		System.out.println(fila.element()); // lança uma exceção
		System.out.println(fila.element());
		
		// Poll e Remove -> obter e remover o próximo elementos da fila
		// Diferença é o comportamento ocorre quando a fila está vazia!
		System.out.println(fila.poll()); // retorna null
		System.out.println(fila.poll());
		System.out.println(fila.poll());
		System.out.println(fila.size());
		System.out.println(fila.remove()); // lança uma exceção
		
		for (String string : fila) {
			System.out.println(string);
		}

		// System.out.println(fila.size());
		// fila.clear();
		// System.out.println(fila.isEmpty()); // Teste para quando tentar remover algo e verificar se a fila está vazia
		// System.out.println(fila.contains("Gui"));
		fila.forEach(lista -> { System.out.println(lista); });
		fila.forEach( System.out::println );
	}
}
